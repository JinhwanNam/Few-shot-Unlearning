class CustomDataset(Dataset):
    def __init__(self, x, y, tf):
        self.data = x.detach().cpu()
        self.targets = y.detach().cpu()
        self.tf = tf
        if len(self.data.shape) == 3:
            self.data = self.data.unsqueeze(dim=1)

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        x = self.tf(self.data[idx])
        y = self.targets[idx]

        return x, y



def softXEnt(inputs, targets):
    if len(targets.shape) < 2:
        num_classes = inputs.shape[1]
        targets = torch.nn.functional.one_hot(targets, num_classes=num_classes).float()

    logprobs = torch.nn.functional.log_softmax(inputs, dim = 1)

    ent = targets * logprobs

    ent = torch.where(ent<10, -ent, torch.tensor(10.).float().to(device))

    return  ent.sum() / inputs.shape[0]



class Normalize(nn.Module) :
    def __init__(self, mean, std) :
        super(Normalize, self).__init__()
        self.register_buffer('mean', torch.Tensor(mean))
        self.register_buffer('std', torch.Tensor(std))
                                                
    def forward(self, inputs):
        mean = self.mean.reshape(1, 3, 1, 1)
        std = self.std.reshape(1, 3, 1, 1)
        return (inputs - mean) / std


